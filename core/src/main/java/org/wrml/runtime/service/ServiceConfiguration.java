package org.wrml.runtime.service;

import org.wrml.runtime.Configuration;

/**
 * The {@link Configuration} for a {@link Service}.
 */
public interface ServiceConfiguration extends Configuration {

    /**
     * The fully qualified class name of the {@link Service} implementation.
     *
     * @return The fully qualified class name of the {@link Service} implementation.
     * @see Class#getName()
     */
    String getImplementation();

    /**
     * The configuration given name of the {@link Service}.
     *
     * @return The user friendly name given to the {@link Service}.
     */
    String getName();

}
